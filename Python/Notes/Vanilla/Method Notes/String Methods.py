# Python String Methods Notes and Examples

# capitalize()
print("'hello WORLD'.capitalize() ->", 'hello WORLD'.capitalize())  # Hello world

# lower()
print("'HELLO WORLD'.lower() ->", 'HELLO WORLD'.lower())  # hello world

# upper()
print("'hello world'.upper() ->", 'hello world'.upper())  # HELLO WORLD

# count()
print("'HELLO WORLD'.count('L') ->", 'HELLO WORLD'.count('L'))  # 3

# index()
print("'HELLO WORLD'.index('E') ->", 'HELLO WORLD'.index('E'))  # 1

# find()
print("'HELLO WORLD'.find('OR') ->", 'HELLO WORLD'.find('OR'))  # 7

# replace()
print("'27/11/2022'.replace('/', '-') ->", '27/11/2022'.replace('/', '-'))  # 27-11-2022

# split()
print("'27/11/2022'.split('/') ->", '27/11/2022'.split('/'))  # ['27', '11', '2022']

# isalnum()
print("'abc123'.isalnum() ->", 'abc123'.isalnum())  # True

# isnumeric()
print("'12345'.isnumeric() ->", '12345'.isnumeric())  # True

# center()
print("'Python'.center(10, '*') ->", 'Python'.center(10, '*'))  # **Python**

# islower()
print("'hello world'.islower() ->", 'hello world'.islower())  # True

# isupper()
print("'HELLO WORLD'.isupper() ->", 'HELLO WORLD'.isupper())  # True

